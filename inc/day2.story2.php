    <div class="large-7 large-offset-3 columns story">      
      <p>In one lecture for the class, Boneh said she approached the topic of the LGBT community cautiously to avoid possible hostility from students.</p>
      <p>She said she especially has to be careful to avoid the appearance that she, as a foreigner, is trying to push any sort of opinion on her students.</p>
      <p>Kabwila, as a Malawian with a strong reputation in other human rights activism, does not have to take those worries into consideration.</p>
      <p>From one corner of the room, a student mouths to me, “Having fun?” We had spoken the night before about his views on gay rights, and he had given us some hints about how engaging Kabwila would be.</p>
      <p>He considers himself to be among the most liberal students on campus because he thinks gay people should have rights.</p>
      <p>He does not approve of men having sex with other men and finds it unnatural. But the country has high rates of poverty, unemployment and HIV prevalence, so there are more important issues to worry about than what two people consent to in their private lives, he said. Plus, acknowledging the gay community might make it less likely for them to sleep with women and transmit HIV to them, he said. </p>
      <p>Boneh said her job as an educator is to present an issue and encourage her students to explore the stigmas around it.</p>
      <figure>
        <img src="<?php echo SITE_URL ?>img/day2/web.bob.10.22.Mwiza.picA.jpg" />
        <figcaption>
          <span class="credit">Blaine Ohigashi / Daily Bruin Senior Staff</span>
          Mwiza Nyirenda made a documentary about attitudes toward homosexuality in Malawi. He identifies as straight, but believes it is important to debate the issue.
        </figcaption>
      </figure>
      <p>One of Boneh’s former students, Mwiza Nyirenda, became interested in Malawian attitudes toward the LGBT community and decided to explore them in a documentary.</p>
      <p>He thought of the idea while the students and teachers of Chancellor College were on strike – he was bored and wanted to be productive, so he started to learn filmmaking. Nyirenda identifies as straight, and he said he does not feel strongly for or against gay rights, but he thinks it is important for society, especially college students, to debate the topic, he said.</p>
      <p>So Nyirenda and two friends interviewed locals and national leaders about their opinions.</p>
      <p>In her afternoon lecture, Kabwila brings the kind of discussion Mwiza sought in his video to the classroom. The discussion between the students and Kabwila has been generally light, punctuated by laughter.</p>
      <p>But Kabwila returns to the question of the intersexed child.</p>
      <p>“Where does the child go?” she asks. “I know people laugh about these things. But my friends, sexuality is not a joke.” ■</p>
