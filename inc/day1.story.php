      <p>“What we have agreed is we don’t want them to work against their beliefs … as long as they are nice to these guys when they come in the clinic.” Zulu said. “You don’t have to treat them – welcome them nicely, introduce them to the person that they want to see. And that has worked well for us.”</p>
      <p>But she also holds trainings for other providers interested in treating MSM.</p>
      <p>In Malawi, not every health care professional is a doctor. A range of classifications between doctor and nurse exist, depending on the level of training – it’s common for a clinical officer, the equivalent of a U.S. nurse practitioner, to have the highest rank in a clinic, without going through full medical school.</p>
      <p>This creates medical gaps that Zulu tries to address.</p>
      <p>A clinician or nurse might misdiagnose gonorrhea in the throat, for example – they don’t expect to see the STD there, so they treat it as a normal throat infection, prescribing the wrong medicine and exacerbating the problem. Studies show that untreated throat STDs can lead to throat cancers.</p>
      <p>During the medical trainings, doctors and nurses who are willing to treat MSM are asked to give their names and contact information to the organizers, who can pass those names on to men who need treatment.</p>
      <p>Zulu said CEDEP should do more to ensure that MSM know the resources available to them. Some health care providers who attended the trainings said they have treated few, if any, MSM since they gave their names for referral. </p>
      <p>But other practitioners remain reluctant to provide targeted aid, or to publicize it.</p>
      <p>At trainings, Zulu reminds doctors and nurses of the oath they took to help all people, regardless of who they are. If they say they are promoting illegal activity by treating MSM, leaders at the trainings point out that doctors treat criminals who come to the office from prisons.        	</p>
      <p>That might be because men don’t know where to go for help or who to ask for, Zulu said.</p>
      <p>Chipo’s routine when he comes to the clinic, for example, is different than that of other patients. Instead of waiting, he and his sister, who often accompanies him to the doctor, walk straight to the check-in desk and asks for Blessings, a clinician who has been trained in treating MSM.</p>
