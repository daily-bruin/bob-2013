    <div class="large-7 columns story story-drop">
      <p>The couple sat side by side in a Malawian coffee shop, both sets of arms crossed on top of the table, no parts of them touching. </p>
      <p>One of the men, who asked to be called Pablo for this story, has been clean for a year – he credits that to his boyfriend of six years, who asked him to stop using drugs and drinking for the sake of their relationship. </p>
      <figure>
        <img src="<?php echo SITE_URL ?>img/photo/02.Nelson.Pablo.JPG" />
        <figcaption>
          <span class="credit">Blaine Ohigashi / Daily Bruin Senior Staff</span>
          &ldquo;We always keep our distance even for fear of being stigmatized. &hellip; I want to live in a society where everyone minds his own business.&rdquo; &mdash;&nbsp;Pablo
