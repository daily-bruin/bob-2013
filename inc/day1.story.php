      <p>He lifted his shirt, revealing a stomach that was two different colors. Black spots covered a dark brown abdomen. Most of the itchy marks were concentrated there, but others speckled his arms and face.</p>
      <p>“They are all over,” he said, resting in the sunny food court of a Malawian strip mall. The spots were a negative reaction to a tuberculosis medication that the man, who asked to be called Chipo, had taken.</p>
      <p>Fatima Zulu, who sat next to Chipo under an umbrella, stroked the marks on his left arm as they waited for their pizza.</p>
      <p>“They were much worse,” she said. “They’ve gotten better over time, but they were much worse.”</p>
      <p>Zulu is the community engagement coordinator at the Johns Hopkins University Research Project in Blantyre, Malawi, a southern city and the country’s economic capital. She oversees a clinic that conducts research on sexually transmitted diseases at Queen Elizabeth Central Hospital in Blantyre.</p>
      <p>Chipo is one of the patients she has helped. He is gay, and, as he found out a few years ago, HIV positive. Tuberculosis is the primary cause of death among HIV-positive patients, according to the World Health Organization.</p>
      <p>The drugs did enough to help him regain some health. He returned to his job in advertising, but the spots remain.</p>
      <p>Malawi, a country smaller than the state of California, is tucked into southeastern Africa between Tanzania, Mozambique and Zambia.</p>
