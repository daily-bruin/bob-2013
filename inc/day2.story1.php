      <p>LGBT activists in other countries are using this argument as well – MSM worldwide are 19 times more likely to contract HIV than the general population, and many are also having sex with women in countries like Malawi where homosexuality is outlawed. </p>
      <p>Plus, most people agree that everyone has a right to health services, said Patrick Brenny, who is a former Malawi director for the Joint United Nations Programme on HIV/AIDS, or UNAIDS. He was the Malawi director until July of this year, and moved to Tanzania in August.</p>
      <p>“There is the public health approach that says anyone who needs a service should get it,” Brenny said. “And no one argues about that.”</p>
      <p>The United States Agency for International Development committed $96.8 million in aid to Malawi during the 2013 fiscal year. Of that, the biggest chunk – $26.9 million – went to combatting HIV/AIDS.</p>
      <p>An easy way for donors to measure the effects of their funding is through the clearly defined programs that organizations such as CEDEP implement. </p>
      <p>For example, activists conduct population estimates to convince the government that MSM exist and need health care, and they host trainings that educate different societal groups, like church leaders and doctors, in an effort to change their perceptions about the LGBT community. </p>
      <p>The government placed a moratorium on enforcement because Malawian president Joyce Banda asked parliament to review the laws against homosexuality, according to an email from Steven Nhlane, the president’s spokesman.</p>
      <p>“The moratorium is to allow consultations to take place in an atmosphere that will encourage freedom of expression,” Nhlane wrote.</p>
      <p>But public opinion is against a change.</p>
      <p>When asked about what the government’s priorities should be, laws regarding homosexuality were low on the list for most Malawians, according to the survey from McKay’s UCLA doctoral dissertation. Respondents ranked it fifth out of seven priorities, in between “improving delivery of electricity in the country” and “making laws to deal with polygamy.”</p>
      <p>It is somewhat rare to hear of an LGBT-related arrest, but members of the community say they still fear that police have the power to arrest them and could use the social stigma to intimidate them or extort bribes. </p>
      <p>Most Malawians McKay surveyed said they would not want “homosexuals as neighbors.”</p>
      <p>LGBT activists’ long-term goal is to eliminate social stigma against the LGBT community. They do recognize that the transition will be a slow one, and it will likely be many years until LGBT people are accepted. </p>
      <p>But HIV/AIDS funding has potential human rights implications.</p>
