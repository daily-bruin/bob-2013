      <p>Along the M1, the highway that spans the length of Malawi and serves as its main trade artery, a sign reading “Partners in Hope” greets drivers on their way into the country’s capital of Lilongwe. It leads to an expansive three-part medical center, founded by Dr. Perry Jansen, who was trained at UCLA. </p>
      <p>UCLA works with the Christian medical center to provide private care, a free HIV/AIDS clinic, and training and research throughout the country. </p>
      <p>The free HIV/AIDS wing is called the “Moyo Clinic” – Moyo means “life” in Chichewa, the most commonly spoken language in Malawi. </p>
      <p>Partners in Hope and UCLA denied us access to the medical center, health care providers and patients for this series.</p>
      <p>Before the trip, UCLA Vice Chancellor for Legal Affairs Kevin Reed wrote in a letter that “you will both make this trip at great personal risk to yourselves, and there is great concern within the UCLA administration that your project … will create undue risk for other persons as well as to UCLA programs operating in Malawi.”</p>
      <p>John Hamilton, the clinic director for Partners in Hope and the head of the UCLA Program in Global Health in Malawi, refused to answer any questions when we went to Partners in Hope, because he needed approval to speak with us, and asked us to email questions instead. He then referred those questions to Jansen, the Partners in Hope founder and director, and Thomas Coates, the director of the UCLA Program in Global Health.</p>
      <p>Jansen answered some questions via email. He wrote that the clinic welcomes all patients.</p>
      <p>“(Partners in Hope’s) ethos is to consider all of our … patients worthy of our best,” Jansen wrote.</p>
      <p>UCLA does conduct HIV/AIDS research in Malawi, as well as a clinical rotation program for UCLA medical residents. The university’s research and prevention efforts have focused largely on preventing mother-to-child transmission of HIV, and UCLA receives funding through the U.S. President’s Emergency Plan for AIDS Relief on a project called “EQUIP-Malawi” to train and provide aid to local healthcare providers for better HIV/AIDS care.</p>
      <p>But in other places, UCLA does tackle LGBT issues from a medical standpoint. In South Africa, where the LGBT community has full legal rights, the UCLA Program in Global Health partners with the MAC AIDS Foundation to provide local HIV prevention fellowships. </p>
      <p>A number of the recipients focus on the LGBT community. One program near Capetown provides protection and legal aid for lesbian women who have been through “corrective rape,” the practice of raping lesbians in order to try to make them straight, while another in Johannesburg addresses the prevention needs of refugee male sex workers.</p>
      <p>Meanwhile, the U.S. National Institute of Health is funding multiple UCLA Program in Global Health studies focusing on HIV prevention for MSM in Peru.</p>
      <p>The difference between these countries and Malawi is the spectrum of legal rights. South Africa is, on paper, one of the world’s most LGBT-friendly countries, with legal rights built into the constitution. Human rights violations and hate crimes still happen in those countries, but a legal framework exists to protect the community.</p>
      <p></p>
      <p><strong>Building a case</strong></p>
      <p>While UCLA’s approach to HIV/AIDS is similar to that of most international institutions operating in Malawi, other organizations do direct aid to the LGBT community.</p>
      <p>Stefan Baral, a researcher at the Johns Hopkins Center for Public Health and Human Rights, has been working with the LGBT community in Malawi since about 2006. He works mostly with the Centre for the Development of People, known as CEDEP, Malawi’s primary gay rights organization.</p>
      <figure>
        <img src="<?php echo SITE_URL ?>img/day1/web.bob.10.21.andrea.dunker.picA.jpg" />
        <figcaption>
          <span class="credit">Blaine Ohigashi / Daily Bruin Senior Staff</span>
