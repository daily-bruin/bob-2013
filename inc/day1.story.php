      <p>It is also one of the 76 countries that outlaw homosexual acts, making it difficult for men who have sex with men to receive HIV treatment. </p>
      <p>Men who have sex with men are referred to as “MSM” in medical circles. The term, meant to be apolitical, includes gay and bisexual men, as well as male sex workers and prisoners. </p>
      <p>A 2008 population estimate of about 200 men in Blantyre showed that about 21 percent of men who have sex with men in that area were HIV positive. </p>
      <p>The Malawian government recognized for the first time about four years ago that MSM are a high-risk population for HIV contraction. Many of these men also have sex with women, which can spread the disease. Worldwide, MSM are 19 times more likely to have HIV than the general population.</p>
      <p>But the Malawian government has done little to improve access to care for these men.</p>
      <p>That’s partly because there’s a low acceptance of the gay community from a highly conservative, religious public, and because there is not enough nationwide data to prove that MSM play a significant role in the spread of HIV in Malawi.</p>
