      <p>The perception that homosexuality does not exist in Malawi is a common one, McKay said. Her data shows that more than 75 percent of Malawians either agreed or strongly agreed that “homosexuality is a Western invention.”</p>
      <p>Fortune was fired from the Christian radio station where he worked from 2003 until 2006. He chose to be honest about his orientation when he was hired, and thus began three years of conversion efforts.</p>
      <p>“They told me it’s very wrong to be a Christian and be gay at the same time,” Fortune said. “They told me to change.”</p>
      <p>He tried exorcisms, hoping to rid himself of homosexuality.</p>
      <p>Every Wednesday, Fortune went to pray on a mountain with a pastor.</p>
      <p>He did a 41-day fast, foregoing food from 6 a.m. until 4 p.m.</p>
      <p>“But it did not work.”</p>
      <p>It wasn’t until 2007, when a friend invited him to a launch party for CEDEP, that Fortune realized how many men were like him.</p>
      <p>“Some of the people who I didn’t expect to be gay were at that party. So I went to them and I asked them, ‘Are you sure you are gay?’ And they said, ‘Ah yes, this is how I am,’” Fortune said. “Some people I had met in the streets, but I didn’t know they were also gay.”</p>
      <p>He was fired from another job in 2010, a nonprofit benefiting children, when the organization found out he was gay, he said.</p>
      <p>He got a job with CEDEP assisting with HIV/AIDS prevention research and data collection. </p>
      <p>Working closely with an LGBT organization does have its risks, though.</p>
      <p>In May, someone posted a list of lesbian women in Malawi on Facebook, followed by a list of gay men that was released over three days.</p>
      <p>“I was just praying that my name shouldn’t appear. Please, please, please, I was praying,”  Fortune said. “But it appeared.”</p>
      <p>Fortune, CEDEP leaders and others reported the page to Facebook, and it was shut down, but the lists popped up again within days.</p>
      <p>Even though anti-gay laws are not strictly enforced in Malawi, the likelihood of them being revoked any time soon is extremely slim. No one wants to touch this controversial of a topic with a presidential election next year, Trapence said.</p>
      <p>Legalizing homosexuality wouldn’t solve all the community’s problems, and it likely would not get rid of social stigma. But, Fortune said, it would help recognize gay men as equal to others.</p>
      <p>Fortune told his story over the course of an hour, pausing whenever a waiter passed by. Bright red lights flooded the cafe behind him, but he spoke under a protective blanket of darkness. ■</p>
