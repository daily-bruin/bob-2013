      <p><strong>A vulnerable state</strong></p>
      <p>On an evening in June, two men finished their drinks at a local bar in northern Malawi. </p>
      <figure>
        <img src="<?php echo SITE_URL ?>img/day2/web.bob.10.22.bar.picA.jpg" />
        <figcaption>
          <span class="credit">Blaine Ohigashi / Daily Bruin Senior Staff</span>
          Bars, like this one in a northern Malawian town, are a common meeting place in small towns and villages.
        </figcaption>
      </figure>
      <p>It was like most bars in the country’s small towns and villages – the counter is large enough for a few people to stand by, and most patrons sit outside, surrounded by a makeshift fence of mismatched wooden sticks protruding from the ground.</p>
      <p>It’s the kind of place where you can find people drinking at every hour, with company or completely alone, shelling out around 50 kwacha (about 15 U.S. cents) for a small, sealed plastic bag filled with clear liquid that’s 40 percent alcohol.</p>
      <p>One of those men, who asked to be called Copro, told me his story in July. He spoke in Chichewa, the most common language in Malawi, and a representative from CEDEP translated.</p>
      <p>The CEDEP representative took down Copro’s story as part of a project to document all human rights violations related to the LGBT community in the country. In 2015, the United Nations will conduct a review of human rights records in Malawi. The UN conducts these worldwide assessments periodically to determine what human rights issues need to be addressed and where. </p>
      <p>The project to document gay rights violations in Malawi is a partnership between CEDEP and another Malawian human rights organization, the Centre for Human Rights and Rehabilitation. If the data reveals a pattern of discrimination, the review could help to increase focus on the LGBT community’s human rights concerns in the long run, instead of just HIV/AIDS needs.</p>
      <p>As of July, Copro lived with someone who did not know Copro’s sexual orientation. So instead of taking his boyfriend home after the bar that night, they went to the house of his gay friend, who lives nearby and lets Copro use the house for sex when his boyfriend comes to visit, Copro said.</p>
      <p>Around 8 p.m., they heard a loud banging on the door.</p>
      <p>A group of community police officers had surrounded the house, Copro said. They broke in, catching Copro and his boyfriend having sex.</p>
      <p>The officers forced both men to put their clothes on and took them to the station for the night, Copro said.</p>
      <p>Copro’s arrest happened in a village in Malawi’s northern region, where community police officers are like a neighborhood watch and don’t necessarily follow the national discourse about whether to enforce sodomy laws. They take a case to the village’s or area’s chief, who decides whether to take it to the government-run police.</p>
      <p>On the way to the chief the next morning, Copro’s friend intercepted them and bribed the officers by paying them each the Malawian equivalent of a few U.S. dollars for Copro’s release.</p>
      <p>The community police told the two men that they were never to be seen together again. But they have met since the arrest, and in July they were still a couple.</p>
      <p><strong>A network of trust</strong></p>
      <p>Stories like Copro’s happen in remote places, and the only way that larger organizations like CEDEP find out is through the networks of peer educators – mostly gay men – that they have developed throughout the country. </p>
      <p>These networks are often established using HIV/AIDS funding, because peer educators are tasked with spreading HIV/AIDS prevention messages. In the absence of open forums, they are the ones who often carry out programs that directly impact LGBT lives. </p>
      <p>They meet in bars to discuss HIV prevention, they distribute free condoms and lubrication from CEDEP, they find MSM to participate in studies and gain access to services.</p>
      <figure>
        <img src="<?php echo SITE_URL ?>img/day2/web.bob.10.22.Gift.picA.jpg" />
        <figcaption>
          <span class="credit">Blaine Ohigashi / Daily Bruin Senior Staff</span>
          Gift Trapence is the director of the Centre for the Development of People, Malawi's primary LGBT rights organization.
        </figcaption>
      </figure>
      <p>As of July, there were about 80 peer educators throughout the country. </p>
      <p>Trapence said the peer education model is the most efficient way to connect one of the country’s most hidden populations, because the men already trust each other. They can pass information to former and current partners, or friends of those partners.</p>
      <p>But the peer educators are volunteers, many unemployed, and they function on a very small stipend for communication and transportation, Trapence said.</p>
      <p>Because the peer educators rely mostly on project-based funding from donors, it is difficult to implement long-term strategies through this program. </p>
      <p>“This is volunteer work,” Trapence said. “You find that people end up using their own personal resources to reach out to their peers.” </p>
      <p></p>
      <p><strong>No longer alone</strong></p>
      <p>Just the existence of a gay rights organization in the country affects some Malawian LGBT individuals, regardless of whether they benefit through HIV/AIDS prevention. </p>
      <p>One gay man, who asked to be called Fortune for this story, works for CEDEP.</p>
      <p>We met on a brisk July evening. He sat at a corner table by a hotel pool – he chose the spot for the interview because he wouldn’t see anyone he knew there, and no one would overhear.</p>
      <p>Fortune remembered reading an article in an English-language magazine about gay men when he was 14. </p>
      <p>He knew then that’s what he was.</p>
      <p>At the time, he thought he was the only gay person in all of Malawi, he said. Two years later, he and a friend at his boarding school slept in the same bed and kissed.</p>
      <p>They thought they were the only two gay people in the country.</p>
