      <p>Some doctors and activists from Malawi and around the world have stepped in, but most of the foreign organizations in the country – including UCLA – do not directly address this group. </p>
      <p>MSM in Malawi who we spoke to said they are afraid of seeking medical treatment because they may be outed. </p>
      <p>Consequences could include being disowned by a wife, church, friends or family, and, in rare cases, being arrested.</p>
      <p>Several years ago, before he knew about his HIV status, Chipo sought treatment for a different rectal sexually transmitted disease. He thought that if he went to a private, internationally funded clinic, he would be well taken care of. </p>
      <p>He was wrong.</p>
      <p>“When I was coming from the consultation room, I found all the staff sitting in the lounge waiting to see me, saying that this boy is homosexual, that’s why he contracted the disease. When I was going out, people were peeping through the window, laughing,” Chipo said. The clinic was in Blantyre and was not affiliated with UCLA.</p>
